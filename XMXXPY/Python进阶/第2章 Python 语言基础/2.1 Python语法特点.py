#注释规则
'''
1.单行注释
在Pyhon中，使用“#”作为单行注释的符号。从符号“#”开始直到换行为止，其后面所有的内
容都作为注释的内容而被Python编译器忽略。
语法如下：
#注释内容
2.多行注释
在Python中，并没有一个单独的多行注释标记，而是将包含在一对三引号（”……”)或者（…….
之间，并且不属于任何语句的内容认为是注释。对于这样的代码，解释器将忽略。由于这样的代码可
为多行编写，所以也作为多行注释。
3.中文注释
在Pyhon中，还提供了一种特殊的中文注释。该注释的出现主要是为了解决Python2.x中不支持
直接写中文的问题。虽然在Pyhon 3.x中，该问题已经不存在了。但是为了规范页面的编码，也为了方
便其他人及时了解文件所用的编码，建议在文件开始加上中文注释。
语法格式如下：
#-*-coding:编码-*-
'''


